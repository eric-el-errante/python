my_foods = ['pizza', 'falafel', 'carrot cake', 'cannoli']
friend_foods = ['cake', 'pizza rolls', 'steak', 'hamburger']

print("My favorite foods are:")
for food in my_foods:
    print(food)
print("My friends favorite foods are:")
for food in friend_foods:
    print(food)