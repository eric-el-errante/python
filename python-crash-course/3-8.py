# using sort function and sort method

places = ['Argentina', 'Japan', 'New Zealand', 'Hawaii', 'Alaska']
print("The list of places is:", places)
print("The sorted list of places is:", sorted(places))
print("The original order of places is:", places)
print("The reverse order of places is:", sorted(places,reverse=True))
print("the original order of places is:", places)
places.reverse()
print("The reverse order of places is:", places)
places.reverse()
print('The original order of places is:', places)
places.sort()
print('The sorted places are:', places)
places.sort(reverse=True)
print('The sorted places in reverse are:', places)